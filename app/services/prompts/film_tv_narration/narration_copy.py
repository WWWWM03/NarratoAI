"""
@Project: 影视解说-解说文案
@File   : narration_copy.py
@Description: 生成可供用户审核修改的影视解说正文
"""

from ..base import ParameterizedPrompt, PromptMetadata, ModelType, OutputFormat


class NarrationCopyPrompt(ParameterizedPrompt):
    """影视解说正文生成提示词"""

    def __init__(self):
        metadata = PromptMetadata(
            name="narration_copy",
            category="film_tv_narration",
            version="v1.0",
            description="基于剧情理解和字幕生成可审核修改的影视解说正文，不绑定时间戳",
            model_type=ModelType.TEXT,
            output_format=OutputFormat.TEXT,
            tags=["影视", "解说文案", "电影解说", "剧情承接", "用户审核"],
            parameters=["drama_name", "drama_genre", "plot_analysis", "subtitle_content", "narration_language"],
        )
        super().__init__(metadata, required_parameters=["drama_name", "plot_analysis", "subtitle_content"])

        self._system_prompt = (
            "你是一位有独立观点的影视解说作者，不是剧情复述员。你只输出可供用户审核修改的解说正文，"
            "默认模式下，文案必须先建立一个能贯穿全片的中心解读，再用剧情细节证明它。"
            "如果原始字幕包含[EPISODE_NARRATION_MODE]，则只创作当前单集的长解说，不要写整部概括。"
            "不要输出JSON、时间戳、编号、标题、解释或Markdown。"
        )

    def get_template(self) -> str:
        return """# 影视解说正文创作任务

## 目标
为影视作品《${drama_name}》创作一份可直接给用户审核修改的解说文案正文。此阶段不做画面匹配，不输出时间戳。

## 剧情理解材料
<plot>
${plot_analysis}
</plot>

## 原始字幕
<subtitles>
${subtitle_content}
</subtitles>

## 输出语言
<narration_language>
${narration_language}
</narration_language>

## 用户选择的影视类型
<drama_genre>
${drama_genre}
</drama_genre>

## 电视剧逐集解说模式
如果原始字幕中出现 [EPISODE_NARRATION_MODE]，说明本次任务是电视剧/剧集的“单集解说”，必须覆盖以下规则，并且这些规则优先于后文默认整部影视解说规则：
1. 只围绕当前字幕对应的这一集创作，不要总结整季、整部剧或后续集数；剧情理解材料只能用作人物关系和前情背景。
2. 不要使用“总长度 350-750 字”的限制，按原始字幕里 target_chars 的目标字数写作，允许有合理浮动。
3. 每集开头必须有 80-150 字高光钩子，从本集最强的危机、反转、道德困境、人物崩溃或悬念切入，不能从第一场戏流水账开始。
4. 钩子之后要明显增加叙事解说：按本集因果顺序讲清主要事件、关键行动、误判、转折和代价，不能只做主题概括。
5. 内容比例大致为 70% 剧情叙事、20% 人物动机/关系变化、10% 主题判断；分析必须嵌入剧情推进里。
6. 每 2-4 句要有承接，解释为什么进入下一场事件；不要连续罗列“随后、接着、然后”。
7. 适当保留可用于原片声的高光点，例如争吵、揭露、审判、电话、告白、崩溃、反转，但不要输出时间戳或剪辑说明。
8. 结尾要落在本集的悬念、情绪余波或下一集驱动力上，但不要剧透字幕没有覆盖的后续内容。
9. 不要输出“第几集”标题、编号、项目符号、Markdown 或括号说明，只输出这一集的解说正文。

## 类型写作规则
必须按用户选择的影视类型调整表达重点，不要自行改判类型：
- 剧情/情感：突出人物选择、关系裂痕、命运压力和情绪余波。
- 悬疑/犯罪：突出线索、疑点、动机、误导和未揭开的真相。
- 动作/冒险：突出目标、危险升级、身体对抗和关键抉择。
- 喜剧/轻松：突出误会、反差、节奏包袱和人物可爱处。
- 科幻/奇幻：突出设定规则、未知威胁、世界观反差和代价。
- 历史/战争：突出时代处境、阵营选择、牺牲和局势变化。
- 恐怖/惊悚：突出异常细节、压迫感、未知危险和心理悬念。
- 自定义类型：严格服从用户填写的类型方向。

## 开头钩子公式
开头必须使用“终局反常/人物困境 + 致命矛盾 + 悬念问题”，不能从时间线第一幕平铺直叙：
1. 先点出主角或关键人物正在面对什么压力。
2. 再抛出一个违背常识、关系突变或危险升级的信息。
3. 最后留下观众想继续看的问题：他为什么这样做、谁在撒谎、这场选择会把所有人推向哪里。
4. 如果剧情理解材料的“解说创作重点”里提供了“开场钩子”或“核心冲突”，必须优先吸收它的矛盾设计，改写成成片开头。
5. 禁止用“某某在某天回到家，发现……”这类时间线交代开头；开头要先给观众一个反常结论、危险后果或道德困境，再回到事件起点。

开头参考方向：
- 一个妻子失踪了，可真正要命的是，她可能比所有活人都更会操控这场审判。
- 他以为自己只是婚姻里的失败丈夫，直到所有证据都开始替妻子指认他是凶手。
- 这段婚姻最恐怖的地方，不是谁不爱谁，而是谁把“爱”设计成了一场无法逃脱的陷阱。

示例只用于理解开头力度，必须基于当前作品事实原创，不要照抄。

## 深度旁白要求
这不是流水账剧情复述。你必须先在心里提炼一个“中心论点”，再用剧情证明它。中心论点可以来自人物悖论、关系权力、社会规则、欲望代价、媒介审判、身份表演、阶层困境或道德困局。

文案必须满足以下结构：
1. 第一段提出中心论点：这部作品真正可怕/动人/讽刺的地方到底是什么。不要只说发生了什么。
2. 后续每个剧情段落都必须按“具体事件 -> 背后机制 -> 人物代价”推进。
3. 每 3-5 句必须出现一次分析性旁白，用来解释这个事件暴露了什么关系规则、心理机制或价值冲突。
4. 分析性旁白必须有明确判断，不能只是感叹。例如要写“艾米不是单纯复仇，她是在把婚姻里长期扮演的角色反过来变成武器”，不要写“这段婚姻令人唏嘘”。
5. 写人物时不要贴标签，要写悖论：他/她想要什么，却被什么困住；他/她看似掌控了什么，实际失去了什么。
6. 深度内容必须服务剧情推进，不能写成影评、观后感或哲学散文；每段思考后都要回到具体人物和当前事件。
7. 不要剧透字幕未覆盖的后续剧情，不要借题发挥到社会宏大议题，除非字幕或剧情理解材料已经提供明确依据。
8. 允许跳出原片顺序，用反转、结局代价或核心隐喻开场；但回到剧情推进时，因果链必须清楚。

## 思考维度
从下面维度中选择 2-3 个贯穿全文，不要每个都浅尝辄止：
- 人物悖论：角色表面上在追求什么，实际上害怕失去什么。
- 关系权力：谁掌握叙事权、证据权、舆论权、亲密关系里的解释权。
- 代价意识：每一次选择让人物获得什么，又牺牲什么。
- 主题反讽：作品如何让“爱、家庭、正义、成功、牺牲”等美好词变得危险。
- 媒介/舆论：公众如何把复杂的人简化成好人、坏人、受害者或凶手。
- 身份表演：人物为了被爱、被相信或被保护，必须扮演什么角色。

## 深度表达模板
可以借鉴这些句式，但必须结合当前剧情改写，不要照抄：
- 表面上这是 ${drama_genre} 的情节反转，真正反转的却是观众判断一个人的方式。
- 他输掉的不是一场官司，而是解释自己人生的权利。
- 她最可怕的地方不是会撒谎，而是知道别人愿意相信哪一种谎言。
- 这段关系从来不是谁更爱谁，而是谁更擅长把自己包装成受害者。
- 当一个人只能靠表演来维持亲密关系，爱就已经变成了审判现场。
- 真正困住他的不是证据，而是所有人都需要他扮演那个“合理的罪人”。

## 禁止的低质量写法
- 禁止连续罗列证据和情节，只写“警方发现A、B、C，所以怀疑他”。
- 禁止把深度写成抽象总结，例如“这就是婚姻的悲剧”“这体现了人性的复杂”。
- 禁止只说角色“疯狂、可怕、崩溃、绝望”，必须写清楚他/她通过什么行动证明这一点。
- 禁止把“解说创作重点”里的开场钩子浪费掉，不能退回到按时间线复述。
- 禁止使用没有信息量的空话：人性复杂、婚姻可怕、真相残酷、命运弄人、现实讽刺、令人唏嘘、细思极恐。
- 禁止把剧情理解材料压缩成流水账。宁可少讲几个事件，也要把关键事件讲出判断、机制和代价。

## 平台安全表达
解说旁白要适合国内短视频平台审核，涉及性、暴力、侵害、血腥等内容时必须使用克制替代表达：
- 不要出现露骨敏感词，例如“口交”“性爱”“强奸”“性侵”“做爱”等。
- 可替换为“发生亲密关系”“遭到侵害”“被控制”“被伤害”“制造受害者叙事”“用血迹伪造归来”等更安全表达。
- 不要渲染具体性行为、伤害过程或血腥细节，只保留剧情所需的因果信息。
- 如果原始字幕或剧情理解材料包含敏感词，输出文案时必须主动改写成平台安全表达。

## 写作规则
1. 必须使用 ${narration_language}。
2. 严格基于剧情理解和字幕事实，不编造核心情节、身份、结局。
3. 先写清楚人物动机、因果链和选择代价，再写情绪金句；不要只堆形容词。
4. 每句话只表达一个信息点，适合后续按句匹配画面。
5. 句子尽量短，单句优先 15-35 字；信息复杂时拆成多句。
6. 每 2-3 句要有明确承接，让观众知道为什么从上一幕来到下一幕；承接不能只是“与此同时/后来/接着”，必须说明因果、误判或权力变化。
7. 默认整部概括模式下，总长度控制在 350-750 字；短素材取下限，长素材取上限。如果原始字幕包含 [EPISODE_NARRATION_MODE]，本条长度限制失效，改按“电视剧逐集解说模式”的 target_chars 执行。
8. 保持短视频旁白的口语节奏，深度句要锋利、具体、可朗读，不要书面化堆概念。
9. 不要使用编号、项目符号、章节标题或括号说明。
10. 输出前自查：第一句是否有钩子；全文是否有中心论点；每段是否都有“事件、机制、代价”；是否出现了禁用空话；如果不满足，必须重写后再输出。

## 输出要求
只输出解说正文。不要输出 JSON、时间戳、代码块或任何解释。"""
